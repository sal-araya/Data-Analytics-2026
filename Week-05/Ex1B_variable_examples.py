# Example: def my_function():

# if → Used for conditional statements (runs code only if condition is True).
# Example: if x > 5:

# for → Used to loop through a sequence (like a list or string).
# Example: for i in range(5):

# class → Used to define a ^^class^^, which is a blueprint for creating objects.
# Example: class Student:

# lambda → Creates a ^^anonymous function^^ (a function without a name).
